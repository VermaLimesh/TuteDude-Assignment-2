#Task 2: Sum of Integers from 1 to 50 Using a Loop


sum_of_integers = 0                                              # Initialize the variable sum_of_integers to 0.

# Calculates the sum of all integers in this range.

for i in range(51):                                              # Uses a for loop to iterate over numbers from 1 to 50.
    sum_of_integers += i
print('The Sum of numbers from 1 to 50: ',sum_of_integers)       # Displays the final sum.